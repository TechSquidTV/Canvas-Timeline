import { mkdir, readFile, readdir, rm, writeFile } from 'node:fs/promises';
import { dirname, relative, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import ts from 'typescript';

const scriptDir = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(scriptDir, '..');
const registryPath = resolve(repoRoot, 'apps/www/src/data/react-registry.ts');
const reactIndexPath = resolve(repoRoot, 'packages/react/src/index.ts');
const hookIndexPath = resolve(repoRoot, 'packages/react/src/hooks/index.ts');
const hookMetadataPath = resolve(repoRoot, 'apps/www/src/data/react-hook-metadata.ts');
const hooksDirPath = resolve(repoRoot, 'packages/react/src/hooks');
const rangeScrollbarIndexPath = resolve(repoRoot, 'packages/react/src/rangeScrollbar/index.ts');
const outputDir = resolve(repoRoot, 'apps/www/.generated/react-registry-snippets');
const internalHookAllowlist = new Set(['useTimelineExternalStore', 'useTimelineGeometryRevision']);
const tsconfigPathAliases = loadTsconfigPathAliases();
const publicReactEntrypoints = new Map([
  ['@techsquidtv/canvas-timeline-react', reactIndexPath],
  ['@techsquidtv/canvas-timeline-react/hooks', hookIndexPath],
  ['@techsquidtv/canvas-timeline-react/range-scrollbar', rangeScrollbarIndexPath],
]);
const registrySource = await readFile(registryPath, 'utf8');
const sourceFile = ts.createSourceFile(
  registryPath,
  registrySource,
  ts.ScriptTarget.Latest,
  true,
  ts.ScriptKind.TS
);
const reactRegistryItems = findArrayLiteral(sourceFile, 'reactRegistryItems');
const hookRegistryGroupConfigs = findArrayLiteral(sourceFile, 'hookRegistryGroupConfigs');
const registryStringConstants = findStringConstants(sourceFile);
const hookMetadataSource = await readFile(hookMetadataPath, 'utf8');
const hookMetadataSourceFile = ts.createSourceFile(
  hookMetadataPath,
  hookMetadataSource,
  ts.ScriptTarget.Latest,
  true,
  ts.ScriptKind.TS
);
const hookMetadataItems = findArrayLiteral(hookMetadataSourceFile, 'timelineHookMetadata');
const exportedTimelineHookNames = [...(await collectExportedNames(hookIndexPath))]
  .filter((name) => name.startsWith('use'))
  .sort((hookA, hookB) => hookA.localeCompare(hookB));
const snippets = [];

const timelineHookNames = verifyHookMetadata({
  exportedHookNames: exportedTimelineHookNames,
  hookMetadataItems,
  hookRegistryGroupConfigs,
});
await verifyPublicHookDocs({
  reactRegistryItems,
  registryStringConstants,
  timelineHookNames,
});
await verifyInternalHookAllowlist({ exportedTimelineHookNames });

for (const item of [...reactRegistryItems.elements, ...hookRegistryGroupConfigs.elements]) {
  if (!ts.isObjectLiteralExpression(item)) {
    continue;
  }

  const slug = readStringProperty(item, 'slug');
  const usageCode = readStringProperty(item, 'usageCode');

  if (!slug) {
    throw new Error('React registry item is missing a string slug.');
  }

  if (!usageCode) {
    throw new Error(`React registry item "${slug}" is missing usageCode.`);
  }

  snippets.push({
    code: usageCode,
    fileName: `${slug}.tsx`,
    label: `${slug}.usageCode`,
  });

  const examples = readArrayProperty(item, 'examples');

  if (!examples) {
    continue;
  }

  examples.elements.forEach((example, index) => {
    if (!ts.isObjectLiteralExpression(example)) {
      return;
    }

    const exampleUsageCode = readStringProperty(example, 'usageCode');

    if (!exampleUsageCode) {
      return;
    }

    snippets.push({
      code: exampleUsageCode,
      fileName: `${slug}.example-${index + 1}.tsx`,
      label: `${slug}.examples[${index}].usageCode`,
    });
  });
}

await rm(outputDir, { recursive: true, force: true });
await mkdir(outputDir, { recursive: true });

const snippetFiles = [];
for (const snippet of snippets) {
  const filePath = resolve(outputDir, snippet.fileName);
  snippetFiles.push(filePath);
  await writeFile(
    filePath,
    `// Generated by scripts/verify-react-registry.mjs from ${snippet.label}.\n${snippet.code}\n`
  );
}

const diagnostics = compileSnippets(snippetFiles);

if (diagnostics.length > 0) {
  console.error(formatDiagnostics(diagnostics));
  process.exit(1);
}

console.log(`Verified ${snippets.length} React registry code snippets.`);

function findArrayLiteral(file, variableName) {
  let arrayLiteral;

  file.forEachChild(function visit(node) {
    if (!ts.isVariableStatement(node)) {
      ts.forEachChild(node, visit);
      return;
    }

    for (const declaration of node.declarationList.declarations) {
      if (
        ts.isIdentifier(declaration.name) &&
        declaration.name.text === variableName &&
        declaration.initializer
      ) {
        arrayLiteral = arrayLiteralFromExpression(declaration.initializer);
      }
    }
  });

  if (!arrayLiteral) {
    throw new Error(`Could not find ${variableName} array literal.`);
  }

  return arrayLiteral;
}

function arrayLiteralFromExpression(expression) {
  if (ts.isArrayLiteralExpression(expression)) {
    return expression;
  }

  if (ts.isSatisfiesExpression(expression) || ts.isAsExpression(expression)) {
    return arrayLiteralFromExpression(expression.expression);
  }

  return undefined;
}

function findStringConstants(file) {
  const constants = new Map();

  file.forEachChild((node) => {
    if (!ts.isVariableStatement(node)) {
      return;
    }

    for (const declaration of node.declarationList.declarations) {
      if (!ts.isIdentifier(declaration.name) || !declaration.initializer) {
        continue;
      }

      const initializer = declaration.initializer;

      if (ts.isStringLiteral(initializer) || ts.isNoSubstitutionTemplateLiteral(initializer)) {
        constants.set(declaration.name.text, initializer.text);
      }
    }
  });

  return constants;
}

function readStringProperty(objectLiteral, name) {
  const property = readProperty(objectLiteral, name);

  if (!property || !ts.isPropertyAssignment(property)) {
    return undefined;
  }

  const initializer = property.initializer;

  if (ts.isStringLiteral(initializer) || ts.isNoSubstitutionTemplateLiteral(initializer)) {
    return initializer.text;
  }

  throw new Error(`${name} must be a static string or template literal.`);
}

function readStringPropertyWithConstants(objectLiteral, name, constants) {
  const property = readProperty(objectLiteral, name);

  if (!property || !ts.isPropertyAssignment(property)) {
    return undefined;
  }

  const initializer = property.initializer;

  if (ts.isStringLiteral(initializer) || ts.isNoSubstitutionTemplateLiteral(initializer)) {
    return initializer.text;
  }

  if (ts.isIdentifier(initializer)) {
    return constants.get(initializer.text);
  }

  throw new Error(`${name} must be a static string, template literal, or string const.`);
}

function readArrayProperty(objectLiteral, name) {
  const property = readProperty(objectLiteral, name);

  if (!property || !ts.isPropertyAssignment(property)) {
    return undefined;
  }

  if (ts.isArrayLiteralExpression(property.initializer)) {
    return property.initializer;
  }

  throw new Error(`${name} must be a static array literal.`);
}

function readProperty(objectLiteral, name) {
  return objectLiteral.properties.find((property) => {
    if (!ts.isPropertyAssignment(property)) {
      return false;
    }

    const propertyName = property.name;

    return (
      (ts.isIdentifier(propertyName) && propertyName.text === name) ||
      (ts.isStringLiteral(propertyName) && propertyName.text === name)
    );
  });
}

function verifyHookMetadata({ exportedHookNames, hookMetadataItems, hookRegistryGroupConfigs }) {
  const metadataNames = [];
  const metadataGroups = new Set();

  for (const item of hookMetadataItems.elements) {
    if (!ts.isObjectLiteralExpression(item)) {
      throw new Error('timelineHookMetadata must contain only object literals.');
    }

    const name = readStringProperty(item, 'name');
    const group = readStringProperty(item, 'group');

    if (!name) {
      throw new Error('timelineHookMetadata item is missing a static string name.');
    }

    if (!group) {
      throw new Error(`timelineHookMetadata item "${name}" is missing a static string group.`);
    }

    metadataNames.push(name);
    metadataGroups.add(group);
  }

  const configuredGroups = new Set();

  for (const item of hookRegistryGroupConfigs.elements) {
    if (!ts.isObjectLiteralExpression(item)) {
      throw new Error('hookRegistryGroupConfigs must contain only object literals.');
    }

    const slug = readStringProperty(item, 'slug');

    if (!slug) {
      throw new Error('hookRegistryGroupConfigs item is missing a static string slug.');
    }

    configuredGroups.add(slug);
  }

  assertNoDuplicates('React hook exports', exportedHookNames);
  assertNoDuplicates('timelineHookMetadata names', metadataNames);

  const missingMetadata = exportedHookNames.filter((name) => !metadataNames.includes(name));
  const extraMetadata = metadataNames.filter((name) => !exportedHookNames.includes(name));
  const missingGroups = [...metadataGroups].filter((group) => !configuredGroups.has(group));

  if (missingMetadata.length > 0 || extraMetadata.length > 0 || missingGroups.length > 0) {
    const details = [
      formatList('Missing hook metadata', missingMetadata),
      formatList('Metadata for non-exported hooks', extraMetadata),
      formatList('Metadata groups without registry config', missingGroups),
    ]
      .filter(Boolean)
      .join('\n');

    throw new Error(`Hook docs metadata drift detected.\n${details}`);
  }

  return metadataNames;
}

async function verifyPublicHookDocs({
  reactRegistryItems,
  registryStringConstants,
  timelineHookNames,
}) {
  const publicExportsByImportPath = new Map();

  for (const [importPath, entryPath] of publicReactEntrypoints) {
    publicExportsByImportPath.set(importPath, await collectExportedNames(entryPath));
  }

  const publicHookNames = [
    ...new Set(
      [...publicExportsByImportPath.values()].flatMap((exports) =>
        [...exports].filter((name) => name.startsWith('use'))
      )
    ),
  ].sort((hookA, hookB) => hookA.localeCompare(hookB));
  const primitiveHookApis = findPrimitiveRegistryHookApis(
    reactRegistryItems,
    registryStringConstants
  );
  const primitiveHookNames = primitiveHookApis.map((api) => api.name);
  const documentedHookNames = new Set([...timelineHookNames, ...primitiveHookNames]);
  const undocumentedPublicHooks = publicHookNames.filter((name) => !documentedHookNames.has(name));
  const nonPublicPrimitiveHooks = [];

  for (const api of primitiveHookApis) {
    const exportedNames = publicExportsByImportPath.get(api.importPath);

    if (!exportedNames) {
      nonPublicPrimitiveHooks.push(`${api.name} via unknown import path ${api.importPath}`);
      continue;
    }

    if (!exportedNames.has(api.name)) {
      nonPublicPrimitiveHooks.push(`${api.name} via ${api.importPath}`);
    }
  }

  assertNoDuplicates('primitive hook registry API names', primitiveHookNames);

  if (undocumentedPublicHooks.length > 0 || nonPublicPrimitiveHooks.length > 0) {
    const details = [
      formatList('Public use* exports missing docs classification', undocumentedPublicHooks),
      formatList(
        'Primitive hook registry APIs missing from their import path',
        nonPublicPrimitiveHooks
      ),
    ]
      .filter(Boolean)
      .join('\n');

    throw new Error(`Public React hook docs drift detected.\n${details}`);
  }
}

async function verifyInternalHookAllowlist({ exportedTimelineHookNames }) {
  const hookFileNames = (await collectHookFileNames(hooksDirPath)).sort();
  const exportedTimelineHooks = new Set(exportedTimelineHookNames);
  const unexpectedInternalHooks = hookFileNames.filter(
    (name) => !exportedTimelineHooks.has(name) && !internalHookAllowlist.has(name)
  );
  const staleAllowlistEntries = [...internalHookAllowlist].filter(
    (name) => !hookFileNames.includes(name)
  );

  if (unexpectedInternalHooks.length > 0 || staleAllowlistEntries.length > 0) {
    const details = [
      formatList(
        'Hook files neither exported nor allowlisted as internal',
        unexpectedInternalHooks
      ),
      formatList('Internal hook allowlist entries without matching files', staleAllowlistEntries),
    ]
      .filter(Boolean)
      .join('\n');

    throw new Error(`Internal React hook allowlist drift detected.\n${details}`);
  }
}

async function collectHookFileNames(directory) {
  const entries = await readdir(directory, { withFileTypes: true });
  const names = [];

  for (const entry of entries) {
    const entryPath = resolve(directory, entry.name);

    if (entry.isDirectory()) {
      names.push(...(await collectHookFileNames(entryPath)));
      continue;
    }

    if (/^use[A-Za-z0-9]+\.tsx?$/.test(entry.name)) {
      names.push(entry.name.replace(/\.tsx?$/, ''));
    }
  }

  return names;
}

function findPrimitiveRegistryHookApis(reactRegistryItems, constants) {
  const apis = [];

  for (const item of reactRegistryItems.elements) {
    if (!ts.isObjectLiteralExpression(item)) {
      continue;
    }

    const kind = readStringProperty(item, 'kind');

    if (kind === 'hook') {
      continue;
    }

    const itemApis = readArrayProperty(item, 'apis');

    if (!itemApis) {
      continue;
    }

    const importPath = readStringPropertyWithConstants(item, 'importPath', constants);

    for (const api of itemApis.elements) {
      if (!ts.isObjectLiteralExpression(api)) {
        continue;
      }

      const name = readStringProperty(api, 'name');

      if (name?.startsWith('use')) {
        if (!importPath) {
          throw new Error(`Registry API "${name}" is missing a resolvable importPath.`);
        }

        apis.push({ name, importPath });
      }
    }
  }

  return apis;
}

async function collectExportedNames(entryPath, visited = new Set()) {
  const normalizedPath = resolve(entryPath);

  if (visited.has(normalizedPath)) {
    return new Set();
  }

  visited.add(normalizedPath);

  const source = await readFile(normalizedPath, 'utf8');
  const file = ts.createSourceFile(
    normalizedPath,
    source,
    ts.ScriptTarget.Latest,
    true,
    normalizedPath.endsWith('.tsx') ? ts.ScriptKind.TSX : ts.ScriptKind.TS
  );
  const names = new Set();

  for (const statement of file.statements) {
    if (ts.isExportDeclaration(statement)) {
      const modulePath = ts.isStringLiteral(statement.moduleSpecifier)
        ? statement.moduleSpecifier.text
        : undefined;

      if (statement.exportClause && ts.isNamedExports(statement.exportClause)) {
        for (const specifier of statement.exportClause.elements) {
          names.add(specifier.name.text);
        }
        continue;
      }

      if (modulePath) {
        const resolvedModulePath = resolveExportModulePath(normalizedPath, modulePath);

        if (resolvedModulePath) {
          for (const name of await collectExportedNames(resolvedModulePath, visited)) {
            names.add(name);
          }
        }
      }

      continue;
    }

    if (!statement.modifiers?.some((modifier) => modifier.kind === ts.SyntaxKind.ExportKeyword)) {
      continue;
    }

    if (
      (ts.isFunctionDeclaration(statement) ||
        ts.isClassDeclaration(statement) ||
        ts.isInterfaceDeclaration(statement) ||
        ts.isTypeAliasDeclaration(statement)) &&
      statement.name
    ) {
      names.add(statement.name.text);
      continue;
    }

    if (ts.isVariableStatement(statement)) {
      for (const declaration of statement.declarationList.declarations) {
        if (ts.isIdentifier(declaration.name)) {
          names.add(declaration.name.text);
        }
      }
    }
  }

  return names;
}

function resolveExportModulePath(fromPath, modulePath) {
  const basePath = modulePath.startsWith('.')
    ? resolve(dirname(fromPath), modulePath)
    : resolveAliasedModulePath(modulePath);

  if (!basePath) {
    return undefined;
  }

  const candidates = [
    basePath,
    `${basePath}.ts`,
    `${basePath}.tsx`,
    resolve(basePath, 'index.ts'),
    resolve(basePath, 'index.tsx'),
  ];

  return candidates.find((candidate) => ts.sys.fileExists(candidate));
}

function resolveAliasedModulePath(modulePath) {
  for (const alias of tsconfigPathAliases) {
    const match = modulePath.match(alias.pattern);

    if (!match) {
      continue;
    }

    return alias.replacement.replaceAll('*', match[1] ?? '');
  }

  return undefined;
}

function loadTsconfigPathAliases() {
  const configPath = resolve(repoRoot, 'tsconfig.base.json');
  const configFile = ts.readConfigFile(configPath, (path) => ts.sys.readFile(path));
  const paths = configFile.config?.compilerOptions?.paths ?? {};

  return Object.entries(paths)
    .flatMap(([key, values]) =>
      Array.isArray(values) && typeof values[0] === 'string'
        ? [
            {
              pattern: new RegExp(`^${escapeRegExp(key).replace('\\*', '(.+)')}$`),
              replacement: resolve(repoRoot, values[0]),
            },
          ]
        : []
    )
    .sort((left, right) => right.pattern.source.length - left.pattern.source.length);
}

function escapeRegExp(value) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

function assertNoDuplicates(label, values) {
  const seen = new Set();
  const duplicates = [];

  for (const value of values) {
    if (seen.has(value) && !duplicates.includes(value)) {
      duplicates.push(value);
    }

    seen.add(value);
  }

  if (duplicates.length > 0) {
    throw new Error(`${label} contains duplicates: ${duplicates.join(', ')}`);
  }
}

function formatList(label, values) {
  return values.length > 0 ? `${label}: ${values.join(', ')}` : '';
}

function compileSnippets(files) {
  const configPath = resolve(repoRoot, 'tsconfig.base.json');
  const configFile = ts.readConfigFile(configPath, (path) => ts.sys.readFile(path));

  if (configFile.error) {
    return [configFile.error];
  }

  const baseCompilerOptions = configFile.config.compilerOptions ?? {};
  const parsedConfig = ts.parseJsonConfigFileContent(
    {
      ...configFile.config,
      compilerOptions: {
        ...baseCompilerOptions,
        noEmit: true,
        noUnusedLocals: false,
        noUnusedParameters: false,
        ignoreDeprecations: '6.0',
        baseUrl: '.',
        paths: {
          ...baseCompilerOptions.paths,
          '@techsquidtv/canvas-timeline-react/range-scrollbar': [
            'packages/react/src/rangeScrollbar/index.ts',
          ],
          '@techsquidtv/canvas-timeline-react/timecode-field': [
            'packages/react/src/timecodeField/index.ts',
          ],
          '@techsquidtv/canvas-timeline-react/timecode-input': [
            'packages/react/src/timecodeInput/index.ts',
          ],
          '@base-ui/react/slider': [
            'node_modules/.pnpm/node_modules/@base-ui/react/slider/index.d.ts',
          ],
        },
      },
      files,
      references: [],
    },
    ts.sys,
    repoRoot,
    undefined,
    configPath
  );

  if (parsedConfig.errors.length > 0) {
    return parsedConfig.errors;
  }

  const program = ts.createProgram({
    rootNames: parsedConfig.fileNames,
    options: parsedConfig.options,
  });

  return ts.getPreEmitDiagnostics(program);
}

function formatDiagnostics(diagnostics) {
  const host = {
    getCanonicalFileName: (fileName) => relative(repoRoot, fileName),
    getCurrentDirectory: () => repoRoot,
    getNewLine: () => '\n',
  };

  return ts.formatDiagnosticsWithColorAndContext(diagnostics, host);
}
